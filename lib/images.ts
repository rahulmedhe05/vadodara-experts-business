interface NicheImages {
  hero: string | null;
  content: (string | null)[];
  gallery: string[];
}

export function getNicheImages(nicheSlug: string, _category: string): NicheImages {
  const has = NICHES_WITH_IMAGES.has(nicheSlug);
  return {
    hero: has ? `/images/niches/${nicheSlug}/hero.webp` : null,
    content: [
      has ? `/images/niches/${nicheSlug}/content-1.webp` : null,
      has ? `/images/niches/${nicheSlug}/content-2.webp` : null,
    ],
    gallery: has ? [
      `/images/niches/${nicheSlug}/gallery-1.webp`,
      `/images/niches/${nicheSlug}/gallery-2.webp`,
      `/images/niches/${nicheSlug}/gallery-3.webp`,
    ] : [],
  };
}

export function getImageAlt(nicheName: string, keyword: string): string {
  return `${nicheName} - ${keyword} in Vadodara | VadodaraExperts`;
}
